	.db 0					;no special events
	.db %00000111			;which types of terrain are on the screen (water, land, trees) (REMINDER!!! - WATER IS BIT 0, TREES BIT 2)
	;4th byte in the metatile screen table is which bank to load metatiles from for this screen (256 total banks)
	;each bank can have a maximum of 128 metatiles
	.dw $0000				;Address of where to load "door" ents from (No doors on this screen)
	.db 0																		;which bank of metatiles to load from
	;Once the bank index has been read, $FF is the termination byte
